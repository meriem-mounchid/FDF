int		ft_calcnumb(int n)
{
	int		i;

	i = 0;
	if (n == -2147483648)
		n = n + 1;
	if (n < 0)
	{
		n = -n;
		i++;
	}
	while (n != 0)
	{
		i++;
		n = n / 10;
	}
	return (i);
}
